import customFormatTests from './common'

const invalidValues = [
  'a',
  '1',
  '0.5',

  // returns false when value does not consist of eight groups
  '0000/0',
  '0000:0000/0',
  '0000:0000:0000/0',
  '0000:0000:0000:0000/0',
  '0000:0000:0000:0000:0000/0',
  '0000:0000:0000:0000:0000:0000/0',
  '0000:0000:0000:0000:0000:0000:0000/0',

  // returns false when groups contain non-hex characters
  '000g:0000:0000:0000:0000:0000:0000:0000/0',
  '0000:000g:0000:0000:0000:0000:0000:0000/0',
  '0000:0000:000g:0000:0000:0000:0000:0000/0',
  '0000:0000:0000:000g:0000:0000:0000:0000/0',
  '0000:0000:0000:0000:000g:0000:0000:0000/0',
  '0000:0000:0000:0000:0000:000g:0000:0000/0',
  '0000:0000:0000:0000:0000:0000:000g:0000/0',
  '0000:0000:0000:0000:0000:0000:0000:000g/0',

  // returns false when groups contain negative numbers
  '-0001:0000:0000:0000:0000:0000:0000:0000/0',
  '0000:-0001:0000:0000:0000:0000:0000:0000/0',
  '0000:0000:-0001:0000:0000:0000:0000:0000/0',
  '0000:0000:0000:-0001:0000:0000:0000:0000/0',
  '0000:0000:0000:0000:-0001:0000:0000:0000/0',
  '0000:0000:0000:0000:0000:-0001:0000:0000/0',
  '0000:0000:0000:0000:0000:0000:-0001:0000/0',
  '0000:0000:0000:0000:0000:0000:0000:-0001/0',

  // returns false when invalid IPv6 interface
  '::1',
  '::',
  'FEDC:BA98:7654::FEDC::3210'
]

const validValues = [
  'FFDC:BA98:7654:3210:FEDC:BA98:7654:3210',
  'FF80:0:0:0:8:800:200C:417A'
]

customFormatTests('ipv6-multicast', invalidValues, validValues)
